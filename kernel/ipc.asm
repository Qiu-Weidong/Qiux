[section .text]

[global send   ]
[global receive]

intr_send       equ 0x80
intr_receive    equ 0x90

;===========================================================================*
; 				send				     *
;===========================================================================*/
;*****************************************************************************
; <特权级 1> <特权级 2> <特权级 3> 发送消息
; 用户进程和系统任务执行
;*****************************************************************************/
send:
    mov eax, [esp+4]
    mov ecx, [esp+8]
    int intr_send
    ret
    
;===========================================================================*
; 				receive				     *
;===========================================================================*/
;*****************************************************************************
; <特权级 1> <特权级 2> <特权级 3> 接收消息
; 用户进程和系统任务执行
;*****************************************************************************/
receive:
    mov eax, [esp+4]
    mov ecx, [esp+8]
    int intr_receive
    ret
